bool comp_point(const point &a, const point &b) {
    return a.value >= b.value;
}

void Bitgrid::debug() {
    for (int x = 0; x < gridsize; x++) {
        for (int y = 0; y < gridsize; y++) {
            if (check(x, y))
                cout << "# ";
            else
                cout << "* ";
        }
        cout << endl;
    }
    cout << endl;
}

bool Bitgrid::check(int x, int y) {
    return static_cast<bool>((state[x >> 2] >> ((x % 4) << 4 | y)) & 1LL);
}

void Bitgrid::change(int x, int y) {
    state[x >> 2] ^= (1LL << ((x % 4) << 4 | y));
}

Bitgrid Bitgrid::operator&(const Bitgrid &a) {
    Bitgrid ret;
    for (int i = 0; i < 4; i++) {
        ret.state[i] = state[i] & a.state[i];
    }
    return ret;
}

Bitgrid Bitgrid::operator~() {
    Bitgrid ret;
    for (int i = 0; i < 4; i++) {
        ret.state[i] = ~ state[i];
    }
    return ret;
}

Bitgrid Bitgrid::operator^(const Bitgrid &a) {
    Bitgrid ret;
    for (int i = 0; i < 4; i++) {
        ret.state[i] = state[i] ^ a.state[i];
    }
    return ret;
}

Bitgrid Bitgrid::operator|(const Bitgrid &a) {
    Bitgrid ret;
    for (int i = 0; i < 4; i++) {
        ret.state[i] = state[i] | a.state[i];
    }
    return ret;
}

void Bitgrid::assign(int64_t a, int64_t b, int64_t c, int64_t d) {
    state[0] = a;
    state[1] = b;
    state[2] = c;
    state[3] = d;
}

void Bitgrid::init() {
    assign(0LL, 0LL, 0LL, 0LL);
}

vector<point> Bitgrid::getOne() {
    vector<point> ret;
    for (int i = 0; i < 4; i++) {
        int pos = 0;
        for (long long temp = state[i]; temp; temp ^= (1LL << pos)) {
            pos = __builtin_ctzll(temp);
            point curr;
            curr.value = 0;
            curr.x = i * 4 + pos / 16;
            curr.y = pos % 16;
            ret.push_back(curr);
        }
    }
    return ret;
}

